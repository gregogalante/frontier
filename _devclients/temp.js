
function bytesToHex(byteArray) {
  return byteArray.map(byte => {
      // Ensure the byte is within the range 0-255
      if (byte < 0 || byte > 255) {
          throw new RangeError('Array element out of byte range');
      }
      // Convert byte to hex and pad with zero if necessary
      return byte.toString(16).padStart(2, '0');
  }).join('');
}

